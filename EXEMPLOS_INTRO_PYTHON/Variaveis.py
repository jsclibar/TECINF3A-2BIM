# Tipos de variáveis em Python

x = 10 #Variável do tipo int (inteiro)
print(type(x))  #Mostra o tipo de váriavel x

y = 10.3 #Variável do tipo float (real)
print(type(y)) #Mostra o tipo de váriavel y

z = 'Senac' #Variável do tipo str (cadeia)
print(type(z)) #Mostra o tipo de váriavel z

w = 10 > 2 #Variável que armazena o resultado de um texto lógico - bool (booleana)
print(type(w)) #Mostra o tipo de váriavel w

print('Variável X vale:', x)
print('Variável Y vale', y)
print('Variável Z vale:', z)
print('Variável W vale:', w)